    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String custoFora = tfValor.getText();
        String vezes = tfVezesMes.getText();
        String locomocao = tfPassagem.getText();
        
        Double custoForaConvert = Double.valueOf(custoFora);
        Double vezesConvert = Double.valueOf(vezes);
        Double locomocaoConvert = Double.valueOf(locomocao);
        
//        Contas para gasto mensal e anual
        Double contaMes = (custoForaConvert * vezesConvert) + 
                            (locomocaoConvert * vezesConvert);
        
        Double contaAnual = (contaMes * 12);
        
//        Economia Mensal Porcentagem
        Double ecoPorcentMes = 100 - ((19.9 * 100) / contaMes);
        
//        Economia Anual Porcenetagem
        Double ecoPorcentAnual = 100 - (((19.9 * 12) * 100) / contaAnual);
        
//        Economia Mensal Bruto
        Double ecoMensalBruto = contaMes - 19.9;
        
//        Economia Anual Bruto
        Double ecoAnualBruto = contaAnual - (19.9 * 12);
        
        lblEconomia.setText(String.format("mes bruto R$ %.2f "
                + "ano bruto %.2f e mes %.2f %%  ano %.2f %% ", ecoMensalBruto, 
                ecoAnualBruto, ecoPorcentMes, ecoPorcentAnual));
    }//GEN-LAST:event_btnCalcularActionPerformed

