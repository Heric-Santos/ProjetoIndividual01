    private void btnIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIMCActionPerformed
        String peso = tfPeso.getText();
        String altura = tfAltura.getText();
//        Convertendo dados
        Double pesoConvert = Double.valueOf(peso);
        Double alturaConvert = Double.valueOf(altura);
//        Calculo IMC
        Double calculo = pesoConvert / (alturaConvert * alturaConvert);
        
        if (calculo < 18.5){
            lblMensagem.setText(String.format("O seu IMC é %.2f. De acordo com "
                    + "a sua estatura e seu peso, você está abaixo do peso "
                    + "ideal.",calculo));
            lblMensagem.setForeground(Color.decode("#ff3333"));
        
        }else if(calculo >= 18.5 && calculo <= 24.9){
            lblMensagem.setText(String.format("Fique tranquilo! Seu IMC é %.2f."
                    + " Você está no peso ideal.", calculo));
            lblMensagem.setForeground(Color.green);
        
        }else if(calculo >= 25 && calculo <= 29.9){
            lblMensagem.setText(String.format("O seu IMC é %.2f. De acordo com "
                    + "a sua estatura e seu peso, você está acima do peso "
                    + "ideal.",calculo));
            lblMensagem.setForeground(Color.yellow);
        
        }else if(calculo >= 30 && calculo <= 34.9){
            lblMensagem.setText(String.format("O seu IMC é %.2f. De acordo com "
                    + "a sua estatura e seu peso, você apresenta obesidade de "
                    + "grau 1",calculo));
            lblMensagem.setForeground(Color.decode("#ff3333"));
        
        }else if(calculo >= 35 && calculo <= 39.9){
            lblMensagem.setText(String.format("O seu IMC é %.2f. De acordo com "
                    + "a sua estatura e seu peso, você apresenta obesidade de "
                    + "grau 2",calculo));
            lblMensagem.setForeground(Color.decode("#ff3333"));
        
        }else if(calculo > 40){
            lblMensagem.setText(String.format("O seu IMC é %.2f. De acordo com "
                    + "a sua estatura e seu peso, você apresenta obesidade de "
                    + "grau 3",calculo));
            lblMensagem.setForeground(Color.decode("#ff3333"));
        }
    }//GEN-LAST:event_btnIMCActionPerformed

